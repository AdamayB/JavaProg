public class elseif_practice {
    public static void main(String[] args) {
        int marks= 1;
        if (marks<10){
            System.out.println("Fail");
        }
        else if(marks>=100){
            System.out.println("Topper");
        }
        else{
            System.out.println("You are Avg!!");
        }
    }
}
